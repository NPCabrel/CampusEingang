    "# Zelle 5: CRUD-Funktionen (zum interaktiven Verwenden im Notebook)\n",
    "def add_task(title, category=\"All\", deadline=\"\", link=\"\", notes=\"\"):\n",
    "    data = load_data()\n",
    "    nid = data.get(\"next_id\", 1)\n",
    "    task = {\"id\": nid, \"title\": title, \"category\": category, \"deadline\": deadline, \"link\": link, \"notes\": notes, \"done\": False}\n",
    "    data[\"tasks\"].append(task)\n",
    "    data[\"next_id\"] = nid + 1\n",
    "    save_data(data)\n",
    "    return task\n",
    "\n",
    "def edit_task(task_id, **kwargs):\n",
    "    data = load_data()\n",
    "    task = next((t for t in data[\"tasks\"] if t[\"id\"]==task_id), None)\n",
    "    if not task:\n",
    "        raise ValueError(\"Task not found\")\n",
    "    for k,v in kwargs.items():\n",
    "        if k in task:\n",
    "            task[k] = v\n",
    "    save_data(data)\n",
    "    return task\n",
    "\n",
    "def toggle_done(task_id):\n",
    "    data = load_data()\n",
    "    task = next((t for t in data[\"tasks\"] if t[\"id\"]==task_id), None)\n",
    "    if not task:\n",
    "        raise ValueError(\"Task not found\")\n",
    "    task[\"done\"] = not task.get(\"done\", False)\n",
    "    save_data(data)\n",
    "    return task\n",
    "\n",
    "def delete_task(task_id):\n",
    "    data = load_data()\n",
    "    data[\"tasks\"] = [t for t in data[\"tasks\"] if t[\"id\"]!=task_id]\n",
    "    save_data(data)\n",

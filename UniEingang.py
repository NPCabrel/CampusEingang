    "def tasks_to_df(data):\n",
    "    df = pd.DataFrame(data[\"tasks\"])\n",
    "    if df.empty:\n",
    "        df = pd.DataFrame(columns=[\"id\",\"title\",\"category\",\"deadline\",\"link\",\"done\",\"notes\"])\n",
    "    if \"deadline\" not in df.columns:\n",
    "        df[\"deadline\"] = \"\"\n",
    "    def parse_deadline(s):\n",
    "        try:\n",
    "            return datetime.strptime(s, \"%Y-%m-%d\").date()\n",
    "        except:\n",
    "            return None\n",
    "    df[\"deadline_date\"] = df[\"deadline\"].apply(parse_deadline)\n",
    "    today = date.today()\n",
